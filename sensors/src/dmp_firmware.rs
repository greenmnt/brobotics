pub fn enable_dmp<I2C>(i2c: &mut I2C) -> Result<(), I2C::Error>
where
    I2C: Write,
{
    // Wake up MPU6050
    // ---- Wake up IMU ----
    rprintln!("wake up");
    loop {
        match i2c.write(MPU6050_ADDR, &[PWR_MGMT_1, 0x00]) {
            Ok(_) => break,
            //Err(stm32f3xx_hal::i2c::Error::Arbitration) => {
            //rprintln!("I2C Arbitration error, retrying...");
            //}
            Err(e) => {
                rprintln!("I2C error");
                cortex_m::asm::delay(10_000);
            }
        }
    }
    rprintln!("awake");

    // Reset DMP and enable
    i2c.write(MPU6050_ADDR, &[USER_CTRL, 0x80])?; // reset DMP
    i2c.write(MPU6050_ADDR, &[USER_CTRL, 0xC8])?; // enable DMP + FIFO
                                                  // Enable FIFO to output DMP data
    i2c.write(MPU6050_ADDR, &[FIFO_EN, 0x00])?; // DMP controls FIFO, so leave it 0

    // Optional: small delay to let DMP start
    cortex_m::asm::delay(8_000_000 / 10); // ~0.1s at 8 MHz

    Ok(())
}

pub fn init_mpu<I2C>(i2c: &mut I2C) -> Result<(), I2C::Error>
where
    I2C: Write,
{
    write_dmp_firmware(i2c)?;
    enable_dmp(i2c)?;
    Ok(())
}

