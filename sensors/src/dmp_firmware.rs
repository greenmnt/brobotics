use embedded_hal::blocking::i2c::Write;
use rtt_target::rprintln;

pub const MPU6050_ADDR: u8 = 0x68;
pub const BANK_SEL: u8 = 0x6D;
pub const MEM_START_ADDR: u8 = 0x6E;
pub const MEM_R_W: u8 = 0x6F;
pub const USER_CTRL: u8 = 0x6A;
pub const PWR_MGMT_1: u8 = 0x6B;
pub const FIFO_EN: u8 = 0x23;

pub fn write_dmp_firmware<I2C>(i2c: &mut I2C) -> Result<(), I2C::Error>
where
    I2C: embedded_hal::blocking::i2c::Write,
{
    const BANK_SIZE: usize = 256;

    for (bank, chunk) in DMP_FIRMWARE.chunks(BANK_SIZE).enumerate() {
        // 1. Select memory bank
        i2c.write(MPU6050_ADDR, &[BANK_SEL, bank as u8])?;

        // 2. Set start address to 0
        i2c.write(MPU6050_ADDR, &[MEM_START_ADDR, 0x00])?;

        // 3. Write firmware bytes sequentially
        for &byte in chunk {
            i2c.write(MPU6050_ADDR, &[MEM_R_W, byte])?;
        }
    }

    Ok(())
}

